package com.collection_framework.java;

public class Book {
	
	int Bk_id;    
	String Bk_name,Bk_author,Bk_publisher;    
	int Bk_quantity;    
	
	Book(int Bk_id,String Bk_name,String Bk_author,String Bk_publisher,int Bk_quantity){
		this.Bk_id = Bk_id;
		this.Bk_name = Bk_name;
		this.Bk_author = Bk_author;
		this.Bk_publisher = Bk_publisher;
		this.Bk_quantity = Bk_quantity;
	}

}
